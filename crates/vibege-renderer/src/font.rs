const FONT_COLS: u32 = 16;
const FONT_ROWS: u32 = 6;
const CHAR_W: u32 = 8;   // glyph width in pixels
const CHAR_H: u32 = 8;   // glyph height in pixels
const FIRST_CHAR: u8 = b' ';
const LAST_CHAR: u8 = b'~';

/// Return RGBA pixel data for the full font atlas texture (128×48 pixels).
/// Each pixel is 4 bytes (R, G, B, A). Lit pixels are white; background is transparent.
pub fn font_atlas_rgba() -> Vec<u8> {
    let tex_w = FONT_COLS * CHAR_W; // 128
    let tex_h = FONT_ROWS * CHAR_H; // 48
    let mut pixels = vec![0u8; (tex_w * tex_h * 4) as usize];

    for ch in FIRST_CHAR..=LAST_CHAR {
        let idx = (ch - FIRST_CHAR) as usize;
        let col = idx as u32 % FONT_COLS;
        let row = idx as u32 / FONT_COLS;
        let base_x = col * CHAR_W;
        let base_y = row * CHAR_H;

        for gy in 0..CHAR_H {
            let byte = FONT_BITS[idx * 8 + gy as usize];
            for gx in 0..CHAR_W {
                let bit = (byte >> (7 - gx)) & 1;
                if bit != 0 {
                    let px = (base_x + gx) as usize;
                    let py = (base_y + gy) as usize;
                    let pi = (py * tex_w as usize + px) * 4;
                    if pi + 3 < pixels.len() {
                        pixels[pi] = 0xFF;     // R
                        pixels[pi + 1] = 0xFF; // G
                        pixels[pi + 2] = 0xFF; // B
                        pixels[pi + 3] = 0xFF; // A (opaque)
                    }
                }
            }
        }
    }
    pixels
}

/// Calculate the pixel width of a string at a given pixel-per-char scale.
#[allow(dead_code)]
pub fn text_width(text: &str, char_w: f32) -> f32 {
    text.chars().count() as f32 * char_w
}
